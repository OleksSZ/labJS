function першийОкремо(перший, ...решта) {
    const обєднанийРядок = решта.join(' ');
    return перший + ' - ' + обєднанийРядок;
}


console.log(першийОкремо("Привіт", "світ", "це", "тест"));  


console.log(першийОкремо(100, "яблука", "груші", "банани"));  


console.log(першийОкремо("Користувач", "admin", "password123"));  
