const readline = require('readline');

class Автомобіль {
  constructor(марка, модель, рікВипуску) {
    this.марка = марка;
    this.модель = модель;
    this.рікВипуску = рікВипуску;
    this.станАвто = "єй, да я перший господарь, не бита не крашена )";
  }

  показатиІнфо() {
    console.log("---------------------------------------");
    console.log("          Автомобіль");
    console.log("---------------------------------------");
    console.log(`Марка:       ${this.марка}`);
    console.log(`Модель:      ${this.модель}`);
    console.log(`Рік випуску: ${this.рікВипуску}`);
    console.log(`Стан авто:   ${this.станАвто}`);
    console.log("---------------------------------------");
  }
}


// Інтерфейс для введення даних
const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

function запитатиДані() {
  rl.question("Марка автомобіля: ", (марка) => {
    rl.question("Модель: ", (модель) => {
      rl.question("Рік випуску: ", (рік) => {
        
        const рікЧисло = Number(рік.trim());
        
        if (isNaN(рікЧисло) || рікЧисло < 1900 || рікЧисло > 2100) {
          console.log("→ Рік має бути числом між 1900 і 2100. Спробуй ще раз.\n");
          запитатиДані();
          return;
        }

        // створюємо об'єкт
        const авто = new Автомобіль(
          марка.trim(),
          модель.trim(),
          рікЧисло
        );

        // виводимо результат
        console.clear();
        авто.показатиІнфо();

        rl.question("\nЗробити ще один запис? (так / ні): ", (відповідь) => {
          const відповідьМала = відповідь.trim().toLowerCase();
          if (відповідьМала === "так" || відповідьМала === "т" || відповідьМала === "yes" || відповідьМала === "y") {
            console.log("\n");
            запитатиДані();
          } else {
            console.log("\nДо побачення! 🚗");
            rl.close();
          }
        });
      });
    });
  });
}

// Запускаємо програму
console.log("Додаймо твій автомобіль до бази 😎\n");
запитатиДані();