function отриматиКлючі(obj) {
    return Object.keys(obj);
}

// приклади
console.log(отриматиКлючі({ a: 2, b: 1, c: 3 }));         
// ["a", "b", "c"]

console.log(отриматиКлючі({ імя: "Олег", вік: 25, місто: "Київ" }));  
// ["ім'я", "вік", "місто"]

console.log(отриматиКлючі({}));                           
// []