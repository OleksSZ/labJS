class Тварина {
  constructor(імя, вік) {
    this.імя = імя;
    this.вік = вік;
  }


  говорити() {
    console.log(`${this.імя} видає якийсь звук...`);
  }

  представитися() {
    console.log(`Я ${this.імя}, мені ${this.вік} ${this.вік === 1 ? 'рік' : 'років'}.`);
  }
}


class Собака extends Тварина {
  говорити() {
    console.log(`${this.імя} каже: Гав-гав!`);
  }
}

class Кіт extends Тварина {
  говорити() {
    console.log(`${this.імя} каже: Мяу-мяу...`);
  }
}

class Корова extends Тварина {
  говорити() {
    console.log(`${this.імя} каже: Мууууу!`);
  }
}

class Папуга extends Тварина {
  говорити() {
    console.log(`${this.імя} каже: Привіт! Як справи? Крааа!`);
  }
}


// Створюємо кілька екземплярів

const тварини = [
  new Собака("Рекс", 4),
  new Кіт("Мурка", 2),
  new Корова("Зорька", 5),
  new Папуга("Кешка", 3),
  new Тварина("Невідома істота", 1)
];


console.log("=== Тварини говорять ===\n");

for (const тварина of тварини) {
  тварина.представитися();
  тварина.говорити();
  console.log("─".repeat(30));
}